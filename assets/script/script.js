// CALENDAR
const monthYear = document.getElementById("monthYear");
const datesContainer = document.getElementById("dates");
const prevButton = document.getElementById("prev");
const nextButton = document.getElementById("next");

let currentDate = new Date();

function renderCalendar() {
    const year = currentDate.getFullYear();
    const month = currentDate.getMonth();

    monthYear.textContent = currentDate.toLocaleString('default', { month: 'long', year: 'numeric' });

    // Clear previous dates
    datesContainer.innerHTML = "";

    // Get first day of the month (0 = Sunday, 1 = Monday, ..., 6 = Saturday)
    const firstDay = new Date(year, month, 1).getDay();
    // Adjust firstDay to make Monday the first day of the week
    const adjustedFirstDay = (firstDay === 0) ? 6 : firstDay - 1; // Sunday becomes 6

    const lastDate = new Date(year, month + 1, 0).getDate();

    // Fill in the blank spaces before the first day
    for (let i = 0; i < adjustedFirstDay; i++) {
        const blankDate = document.createElement("div");
        datesContainer.appendChild(blankDate);
    }

    // Fill in the dates
    for (let date = 1; date <= lastDate; date++) {
        const dateElement = document.createElement("div");
        dateElement.textContent = date;
        dateElement.classList.add("date");

        // Get the current date and the next three days
        const today = currentDate.getDate();
        const upcomingDates = [today, today + 1, today + 2, today + 3];

        // Check if the date is today or one of the next three days
        if (upcomingDates.includes(date)) {
            dateElement.style.backgroundColor = "#0069ff11";
            dateElement.style.color = "#0060e6";
            dateElement.style.fontWeight = "600";
            dateElement.style.cursor = "pointer";
            dateElement.classList.add('hoverable');

            // Assign onclick functions based on the date
            if (date === today) {
                dateElement.onclick = showAside1; // Current date
            } else if (date === today + 1) {
                dateElement.onclick = showAside2; // Next day
            } else if (date === today + 2) {
                dateElement.onclick = showAside3; // Day after next
            } else if (date === today + 3) {
                dateElement.onclick = showAside4; // Three days ahead
            }
        }

        // Check if the date is today
        if (date === today && month === currentDate.getMonth() && year === currentDate.getFullYear()) {
            // Create the dot element
            const dotElement = document.createElement("div");
            dotElement.style.width = "5px";
            dotElement.style.height = "5px";
            dotElement.style.borderRadius = "50%";
            dotElement.style.backgroundColor = "#0060e6";
            dotElement.style.margin = "0 auto"; // Center the dot
            dotElement.style.position = "relative";
            dotElement.style.top = "1px"; // Adjust the position to be beneath the date
            dateElement.appendChild(dotElement);
        }

        datesContainer.appendChild(dateElement);
    }
}

prevButton.addEventListener("click", () => {
    currentDate.setMonth(currentDate.getMonth() - 1);
    renderCalendar();
});

nextButton.addEventListener("click", () => {
    currentDate.setMonth(currentDate.getMonth() + 1);
    renderCalendar();
});

// Initial render
renderCalendar();
